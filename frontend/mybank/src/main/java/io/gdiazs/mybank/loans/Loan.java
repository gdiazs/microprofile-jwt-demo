package io.gdiazs.mybank.loans;

public class Loan {

	private String loanNumber;
	
	private String loanAlias;
	
	private String loanFee;
	
	private String loanBalance;
	
	private String loanDatePayment;

	public String getLoanNumber() {
		return loanNumber;
	}

	public void setLoanNumber(String loanNumber) {
		this.loanNumber = loanNumber;
	}

	public String getLoanAlias() {
		return loanAlias;
	}

	public void setLoanAlias(String loanAlias) {
		this.loanAlias = loanAlias;
	}

	public String getLoanFee() {
		return loanFee;
	}

	public void setLoanFee(String loanFee) {
		this.loanFee = loanFee;
	}

	public String getLoanBalance() {
		return loanBalance;
	}

	public void setLoanBalance(String loanBalance) {
		this.loanBalance = loanBalance;
	}

	public String getLoanDatePayment() {
		return loanDatePayment;
	}

	public void setLoanDatePayment(String loanDatePayment) {
		this.loanDatePayment = loanDatePayment;
	}
	
	
	
	
}
